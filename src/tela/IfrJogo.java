    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String idTabela = String.valueOf(tblJogo.getValueAt(tblJogo.getSelectedRow(), 0));
        idJogo = Integer.parseInt(idTabela); // método para transformar a string idTabela no int idFornecedor 

        //imprimindo no console o id selecionado para confirmar que o mesmo está ok durante a aplicaçaõ
        System.out.println(idJogo);

        // recebendo o retorno do método consultar id, passando o int idEndereco como parametro 
        // e criando um novo objeto do tipo endereco para passar seu atributos

        // testando para verificar se é nulo ou não
        // se for nulo, quer dizer que está ok conforme o método excluir na classe EnderecoDAO e 
        // será feita a exlusão
        if (new JogoDAO().excluir(idJogo) == null) {          
            JOptionPane.showMessageDialog(this, "Exclusão feita com sucesso!");
            
            new JogoDAO().popularTabela(tblJogo, ""); // gera novamente a table depois da exlusão
            
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir JOGO!");
        }        
        idJogo = 0;
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String idTabela = String.valueOf(tblJogo.getValueAt(tblJogo.getSelectedRow(), 0));
        idJogo = Integer.parseInt(idTabela); // método para transformar a string idTabela no int idFornecedor 

        //imprimindo no console o id selecionado para confirmar que o mesmo está ok durante a aplicaçaõ
        System.out.println(idJogo);

        // recebendo o retorno do método consultar id, passando o int idFornecedor como parametro 
        // e criando um novo objeto do tipo endereco para passar seu atributos
        Jogo jogo = new JogoDAO().consultarId(idJogo);

        // se for diferente de nulo, quer dizer que o objeto foi criado e preenchido
        if (jogo != null) {
            jTabbedPane1.setSelectedIndex(1); // faz trocar para a aba de manutenção da aplicação ao invés da consulta
            tfdNome.setText(jogo.getNome()); // define o texto do text field de manutenção como o mesmo presente no objeto endereco em que foi
            // encontrado o id passado como parametro
            tffDataLancamento.setText(Formatacao.removerFormatacao(Formatacao.ajustaDataDMA(jogo.getData_lancamento())));
            tffValor.setText(String.valueOf(jogo.getValor()));
            cmbTipo.setSelectedItem(jogo.getTipo_id());
            

            tfdNome.requestFocus(); // deixa o mouse em cima desse text field
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao editar Jogo: ");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

