    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       
        String nomeJogo = tfdNome.getText();
        String dataLancamento = tffDataLancamento.getText();
        Double valor = Double.valueOf(tffValor.getText());
        ComboItem ciTipo = (ComboItem) cmbTipo.getSelectedItem();
        

        Jogo jogo = new Jogo();

        jogo.setId(idJogo);
        jogo.setData_lancamento(dataLancamento);
        jogo.setNome(nomeJogo);
        jogo.setTipo_id(ciTipo.getId());
        jogo.setValor(valor);

        JogoDAO jogoDAO = new JogoDAO();
        if (idJogo == 0) {
            if (jogoDAO.salvar(jogo) == null) {
                tfdNome.setText("");
                tffDataLancamento.setText("");
                tffValor.setText("");
                cmbTipo.setSelectedItem("Selecione");

                JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");
            } else { 
                JOptionPane.showMessageDialog(this, "Problemas ao salvar registro!");
            }
        } else { 
            if (jogoDAO.atualizar(jogo) == null) {
                
                tfdNome.setText("");
                tffDataLancamento.setText("");
                tffValor.setText("");
                cmbTipo.setSelectedItem("Selecione");

                JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!");
            } else { 
                JOptionPane.showMessageDialog(this, "Problemas ao salvar registro!");
            }
        }
        idJogo = 0; 
        new JogoDAO().popularTabela(tblJogo, ""); 
        jTabbedPane1.setSelectedIndex(0); 
    }//GEN-LAST:event_btnSalvarActionPerformed

